/** @file Ostap/MakeArray.h
 *  helper utility to crate compile-time array 
 *  @author Vanya BELYAEV Ivan.Belyaev@itep.ru
 *  @date 2010-04-19
 */
// ===========================================================================
namespace Ostap
{
  // ==========================================================================
  namespace Math
  {
    // ========================================================================
    template <typename F, std::size_t ... Is>
      constexpr auto make_array ( F f , std::index_sequence<Is...> )
      -> std::array<std::decay_t<decltype(f(0u))>, sizeof...(Is)>
      { return {{f(Is)...}}; }
    // ======================================================================
  } //                                        The end of namespaxe Ostap::Math
  // ==========================================================================
} //                                                 The end of namespace Ostap 
// ============================================================================
//                                                                      The END 
// ============================================================================
#endif // OSTAP_MAKE_ARRAY_H
