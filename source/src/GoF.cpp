/* K+ function Fro Berk&Jones estimator
 *  @see https://en.wikipedia.org/wiki/Berk-Jones_test
 */
// ============================================================================
double Ostap::GoF::K_plus
( const double t , 
  const double x ) 
{
    if ( 0 < x && x < t && t < 1 )
    {
        const long double xl { x } ;
        const long double tl { t } ; 
        return tl * std::log ( tl / xl ) + ( 1.0L - tl ) * std::log ( ( 1.0L - tl ) / ( 1.0L - xl ) ) ; 
    }
    else if ( ( 0 <= t || s_zero ( t ) ) && ( t <= x || s_equal ( t , x ) ) && ( x <= 1 || s_equal ( x , 1 ) ) ) { return 0 ; } 
    //
    return std::numeric_limits<double>::infinity () ;
}
