/** Get Bernulli number 
 *  - \f$ B_0 = 1 \f$ 
 *  - \f$ B_1 = -\frac{1}{2} \f$ 
 *  - \f$ B_{2k+1) = 0 \f$ 
 */
// ============================================================================
double Ostap::Math::bernulli ( const unsigned short n )
{
  if      ( 0 == n                   ) { return  1   ; }  
  else if ( 1 == n                   ) { return -0.5 ; }  // ATTENTION! 
  else if ( 1 == n % 2               ) { return  0.0 ; }
  else if ( n <= 2 * N_BERNULLI_MAX2 ) { return s_BERNULLI [ n / 2 ] ; }
  //
  typedef std::map<unsigned short,double> MAP   ;
  typedef SyncedCache<MAP>                STORE ;
  /// the actual store 
  static  STORE s_store {} ; // the actual store 
  // ==========================================================================
  { // look into the store
    // ========================================================================
    STORE::Lock lock ( s_store.mutex() ) ;
    auto it = s_store->find ( n ) ;
    if  ( s_store->end() != it ) { return it->second ; }
    // ========================================================================
  } // ========================================================================
  // ==========================================================================
  double result = 0 ; 
  if ( N_CHOOSE_MAX <= n  ) { result = -n * Ostap::Math::zeta ( 1 - n ) ; }
  else
  { // use explicit formulae     
    for ( unsigned short k = 0 ; k < n ; ++ k )
    { result -= bernulli ( k ) * choose ( n , k ) / ( n + 1 - k ) ; }
  }
  // ==========================================================================
  { // update the store
    // ========================================================================
    STORE::Lock lock ( s_store.mutex() ) ;
    s_store->insert ( std::make_pair ( n , result ) ) ;
    // ========================================================================
  } // ========================================================================
  // ==========================================================================
  return result ;
  // ==========================================================================
}
