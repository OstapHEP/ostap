""" Useful utilities for multiprocessing and parallel processing for Ostap
"""
# =============================================================================
__version__ = '$Revision$'
__author__  = 'Vanya BELYAEV Ivan.Belyaev@itep.ru'
__date__    = '2016-02-23'
__all__     = (
    'Task'          , ## the base class for task
    'WorkManager'   , ## task manager
    'GenericTask'   , ## very generic "template"  tasl
    'Checker'       , ## check of the object can be pickled/unpickled 
    )
# =============================================================================
from   ostap.parallel.task import Task, GenericTask
import ostap.core.config   as     config 
import sys, os
# =============================================================================
from ostap.logger.logger import getLogger
if '__main__' == __name__ : logger = getLogger ( 'ostap.parallel.parallel')
else                      : logger = getLogger ( __name__         ) 
# =============================================================================
## possible types of workers 
workers = ( 'PATHOS'          , 
            'FUTURES'         , 
            'JOBLIB'          ,
            'IPYPARALLEL'     , 
            'GAUDIMP'         ,
            'GAUDI'           ,
            'MULTIPROCESS'    ,
            'MULTIPRCCESSING' )
# =============================================================================
## get it from the configuration 
worker  = config.parallel.upper()
if not worker in workers : worker = ''

# =============================================================================
has_dill = True 
# =============================================================================
try : # =======================================================================
    # =========================================================================
    import dill
    dill_version   =  getattr ( dill , '__version__' , '' )
    if not dill_version :  dill_version =  getattr ( dill , 'version' , '' )
    if not dill_version or dill_version < '0.3' :
        logger.error ("Too old version of `dill'")
        has_dill = False
        dill     = None 
    else :
        has_dill = True 
    # =========================================================================
except ImportError : # ========================================================
    # =========================================================================
    dill     = None    
    has_dill = False 
    
# =============================================================================
## Check for pathos
# =============================================================================
if ( not worker  or 'PATHOS' == worker ) and dill : # =========================
    # =========================================================================
    try : # ===================================================================
        # =====================================================================        
        import pathos as _pathos 
        worker = 'PATHOS'
        # =====================================================================
    except ImportError : # ====================================================
        # =====================================================================
         worker = ''
        # =====================================================================

# =============================================================================
## Check for ipyparallel
# =============================================================================
if not worker or 'IPYPARALLEL' == worker : # ==================================
    # =========================================================================
    try : # ===================================================================
        # =====================================================================              
        import ipyparallel as _ipp
        worker = 'IPYPARALLEL' if ( 8 , 0 ) <= _ipp.version_info else ''
        # =====================================================================
    except ImportError : # ====================================================
        # =====================================================================
        worker = ''
        # =====================================================================

# =============================================================================
## Check for futures 
# =============================================================================
if not worker or 'FUTURES' == worker : # ==================================
    # =========================================================================
    try : # ===================================================================
        # =====================================================================              
        import concurrent.futures 
        worker = 'FUTURES' 
        # =====================================================================
    except ImportError : # ====================================================
        # =====================================================================
        worker = ''
        # =====================================================================

# =============================================================================
## Check for joblib 
# =============================================================================
if not worker or 'JOBLIB' == worker : # =======================================
    # =========================================================================
    try : # ===================================================================
        # =====================================================================              
        import joblib 
        worker = 'JOBLIB' 
        # =====================================================================
    except ImportError : # ====================================================
        # =====================================================================
        worker = ''
        # =====================================================================
        
# ===============================================================================
WorkManager = None

# ===============================================================================
## Use ipyparallel?
# ===============================================================================
if 'IPYPARALLEL' == worker :
    # ===========================================================================
    try : # =====================================================================
        # =======================================================================
        from ostap.parallel.parallel_ipyparallel import WorkManager, Checker
        logger.debug ('Use WorkManager from ostap.parallel.parallel_ipyparallel')
        # =======================================================================
    except ImportError : # ======================================================
        # =======================================================================
        WorkManage, Checker = None, None
        worker      = 'PATHOS' if has_dill else 'FUTURES'

# ===============================================================================
## Use concurrent futures ? 
# ===============================================================================
if 'FUTURES' == worker :
    # ===========================================================================
    try : # =====================================================================
        # =======================================================================
        from ostap.parallel.parallel_futures import WorkManager, Checker
        logger.debug ('Use WorkManager from ostap.parallel.parallel_futures')
        # =======================================================================
    except ImportError : # ======================================================
        # =======================================================================
        WorkManage, Checker = None, None
        worker      = 'PATHOS' if has_dill else 'JOBLIB'

# ===============================================================================
## Use joblib ? 
# ===============================================================================
if 'JOBLIB' == worker :
    # ===========================================================================
    try : # =====================================================================
        # =======================================================================
        from ostap.parallel.parallel_joblib import WorkManager, Checker
        logger.debug ('Use WorkManager from ostap.parallel.parallel_joblib')
        # =======================================================================
    except ImportError : # ======================================================
        # =======================================================================
        WorkManage, Checker = None, None
        worker      = 'PATHOS'
        
# ===============================================================================
## Use PATHOS?
# ===============================================================================
if 'PATHOS' == worker and dill and not WorkManager :
    # ===========================================================================
    try : # =====================================================================
        from ostap.parallel.parallel_pathos import WorkManager, Checker
        logger.debug ('Use WorkManager from ostap.parallel.parallel_pathos')
        # =======================================================================
    except ImportError : # ======================================================
        # =======================================================================
        WorkManager, Checker = None, None

# ===============================================================================
## Use multiprocess/multiprocessing
# ===============================================================================
if not WorkManager : # ==========================================================
    # ===========================================================================
    from ostap.parallel.parallel_gaudi  import WorkManager, Checker
    logger.debug ('Use WorkManager from ostap.parallel.parallel_gaudi' )
    worker = 'GAUDI'

if not Checker : from ostap.io.pickling import Checker

# ===============================================================================
if '__main__' == __name__ :
    
    from ostap.utils.docme import docme
    docme ( __name__ , logger = logger )

# =============================================================================
##                                                                      The END 
