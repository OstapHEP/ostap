__all__     = (
    'HAS_AVX'   , ## AVX is supported ? 
    'HAS_AVX2'  , ## AVX2 is supported ?
    'cpu_info'  , ## cpu-info 
    )
# =============================================================================
import sys
# =============================================================================
HAS_AVX  = False
HAS_AVX2 = False
cpu_info = {} 
# =============================================================================
try : # =======================================================================
    # =========================================================================
    import cpuinfo
    cpu_info.update ( cpuinfo.cpuinfo.get_cpu_info() )
    HAS_AVX  = 'avx'  in cpu_info.get ( 'flags' , () )
    HAS_AVX2 = 'avx2' in cpu_info.get ( 'flags' , () )    
    # =========================================================================
except ImportError : # ========================================================
    # =========================================================================
    cpu_info = {}
# =============================================================================
if not cpu_info : # ===========================================================
    # =========================================================================
    try : # ===================================================================
        # =====================================================================
        import cpufeature
        cpu_info.update ( cpufeature.CPUFeature )
        HAS_AVX  = cpu_info.get ( 'AVX'  , False )
        HAS_AVX2 = cpu_info.get ( 'AVX2' , False )
        # =====================================================================
    except ImportError : # ====================================================
        # =====================================================================
        cpu_info = {}
# =============================================================================

# =============================================================================
if ( 3 , 15 ) <= sys.version_info : # =========================================
    # =========================================================================
    cpu_info = frozendict ( cpu_info ) 
        
